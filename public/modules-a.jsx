// --- Known people registry for click-to-lens text rendering -------------------
// Configured per-user via window.SEED.knownPeople (set by data-override.jsx,
// generated by the dashboard skill from your config). Each entry:
//   { match: '<text to detect>', name: '<full name>', note: '<role>', manager?: true }
// Order matters — longer/more specific patterns first so a full-name match
// wins over a first-name match. If empty, name detection is disabled.
const KNOWN_PEOPLE = (window.SEED && Array.isArray(window.SEED.knownPeople)) ? window.SEED.knownPeople : [];

const PEOPLE_REGEX = (() => {
  if (!KNOWN_PEOPLE.length) return null;
  const escape = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  const sorted = [...KNOWN_PEOPLE].sort((a, b) => b.match.length - a.match.length);
  const alt = sorted.map(p => escape(p.match)).join('|');
  return new RegExp(`\\b(${alt})\\b`, 'g');
})();

// Wrap people-name occurrences in clickable spans that open the Stakeholder Lens.
// Falls back to the raw text when no matches are found.
function TextWithPeople({ text }) {
  if (!text) return null;
  if (!PEOPLE_REGEX) return text; // No people configured → render plain text
  // Reset regex state — RegExp /g objects are stateful across exec calls.
  PEOPLE_REGEX.lastIndex = 0;
  const parts = [];
  let lastIdx = 0;
  let m;
  let key = 0;
  while ((m = PEOPLE_REGEX.exec(text)) !== null) {
    if (m.index > lastIdx) parts.push(text.slice(lastIdx, m.index));
    const matchedText = m[0];
    const person = KNOWN_PEOPLE.find(p => p.match === matchedText)
                || KNOWN_PEOPLE.find(p => p.match.toLowerCase() === matchedText.toLowerCase());
    if (person) {
      parts.push(
        <span
          key={`p-${key++}`}
          className="text-person"
          onClick={(e) => {
            e.stopPropagation();
            window.dispatchEvent(new CustomEvent('dash:open-lens', { detail: { person } }));
          }}
          title={`Open lens for ${person.name}`}
          role="button"
          tabIndex={0}
        >{matchedText}</span>
      );
    } else {
      parts.push(matchedText);
    }
    lastIdx = m.index + matchedText.length;
  }
  if (lastIdx < text.length) parts.push(text.slice(lastIdx));
  return <>{parts}</>;
}

// --- Auto-prioritization: pure scoring helpers used by Top3 + suggestion line ---
// Score range typically 20-100. Higher = should be done sooner.
// Signals: deadline pressure (bucket), priority field (p), stakeholder seniority
// (text mentions of senior people), date recency (today/this week), blocked penalty.
const SENIOR_RANK = [
  // Order matters — first match wins so the most senior reason surfaces in the chip.
  { re: /\bceo\b/i,                               weight: 25, label: 'CEO' },
  { re: /\b(christopher\s+rogan|rogan)\b/i,       weight: 22, label: 'Rogan' },
  { re: /\bjose\b/i,                              weight: 20, label: 'Jose' },
  { re: /\bchristopher\b/i,                       weight: 15, label: 'Christopher' },
  { re: /\beze(quiel)?\b/i,                       weight: 14, label: 'Eze' },
  { re: /\bpaula\b/i,                             weight: 12, label: 'Paula' },
  { re: /\b(konstantinos|emily|mathew|sagat)\b/i, weight: 10, label: 'partner' },
];

function scoreTask(t, ctx = {}) {
  if (!t || t.done) return { score: 0, reasons: [] };
  let score = 0;
  const reasons = [];

  // Bucket = baseline urgency
  const bucket = ctx.bucket || 'dueSoon';
  if (bucket === 'overdue')        { score += 40; reasons.push('overdue'); }
  else if (bucket === 'top3')      { score += 30; reasons.push('top-3'); }
  else if (bucket === 'dueSoon')   { score += 25; reasons.push('due soon'); }
  else if (bucket === 'blocked')   { score += 15; reasons.push('blocked'); }

  // Priority field on the task itself
  if (t.p === 1)      { score += 20; reasons.push('P0'); }
  else if (t.p === 2) { score += 10; reasons.push('P1'); }

  // Stakeholder seniority — first match in the rank order
  const text = `${t.label || ''} ${t.meta || ''}`;
  for (const rule of SENIOR_RANK) {
    if (rule.re.test(text)) { score += rule.weight; reasons.push(rule.label); break; }
  }

  // Date recency hints in meta
  const metaLc = (t.meta || '').toLowerCase();
  if (/\btoday\b/.test(metaLc))            { score += 15; reasons.push('today'); }
  else if (/\bthis week\b/.test(metaLc))   { score += 8;  reasons.push('this wk'); }
  else if (/\btomorrow\b/.test(metaLc))    { score += 12; reasons.push('tomorrow'); }
  else if (/\bfri(day)?\b/.test(metaLc) || /\bthu(rsday)?\b/.test(metaLc) || /\bwed(nesday)?\b/.test(metaLc))
                                            { score += 5; }

  // OKR alignment via the tagger API (only counts confirmed tags, not suggestions)
  if (ctx.okrApi && typeof ctx.okrApi.getTag === 'function') {
    const tag = ctx.okrApi.getTag(t.label);
    if (tag === 'k1')      { score += 15; reasons.push('k1·AI'); }
    else if (tag)          { score += 10; reasons.push(tag); }
  }

  return { score, reasons };
}

// Score every open task across overdue + dueSoon + blocked + top3, return sorted desc.
function scoreAllOpen(SEED, okrApi) {
  const out = [];
  const push = (arr, bucket) => (arr || []).forEach(t => {
    if (t.done) return;
    const { score, reasons } = scoreTask(t, { bucket, okrApi });
    out.push({ ...t, _bucket: bucket, _score: score, _reasons: reasons });
  });
  push(SEED.top3,    'top3');
  push(SEED.overdue, 'overdue');
  push(SEED.dueSoon, 'dueSoon');
  push(SEED.blocked, 'blocked');
  out.sort((a, b) => b._score - a._score);
  return out;
}

// --- OkrTagger: shared chip + picker used on tasks, top3, decisions, shipped ---
// Three states:
//   confirmed (manual tag) → solid colored chip · click opens picker to change/remove
//   suggested (auto-match) → dashed faint chip with "?"  · click confirms it
//   untagged + no suggestion → tiny "+" appears on row hover · click opens picker
function OkrTagger({ label, meta, okrApi }) {
  const [open, setOpen] = useState(false);
  const rootRef = useRef(null);
  if (!okrApi) return null;
  const tag = okrApi.getTag(label);
  const suggested = !tag ? okrApi.getSuggestion(label, meta) : null;
  const effective = tag || suggested;

  useEffect(() => {
    if (!open) return;
    const handler = (e) => {
      if (rootRef.current && !rootRef.current.contains(e.target)) setOpen(false);
    };
    document.addEventListener('mousedown', handler);
    return () => document.removeEventListener('mousedown', handler);
  }, [open]);

  const okrs = (window.SEED && window.SEED.okrs) || [];
  const okrName = (id) => (okrs.find(o => o.id === id) || {}).name || id;
  const M = okrApi.META || {};

  const onChipClick = (e) => {
    e.stopPropagation();
    if (suggested && !tag) {
      // confirm suggestion
      okrApi.setTag(label, suggested);
    } else {
      setOpen(o => !o);
    }
  };
  const onAddClick = (e) => { e.stopPropagation(); setOpen(o => !o); };

  return (
    <span ref={rootRef} className="okr-tagger" onClick={e => e.stopPropagation()}>
      {effective ? (
        <button
          className={'okr-chip' + (tag ? ' confirmed' : ' suggested')}
          onClick={onChipClick}
          title={tag
            ? `Tagged · ${okrName(tag)} · click to change`
            : `Suggested · ${okrName(suggested)} · click to confirm`}
          style={{
            background: M[effective] && M[effective].bg,
            color:      M[effective] && M[effective].ink,
            borderColor: M[effective] && M[effective].color,
          }}
        >
          {(M[effective] && M[effective].short) || effective}{!tag && '?'}
        </button>
      ) : (
        <button className="okr-add-btn" onClick={onAddClick} title="Tag to OKR">→</button>
      )}
      {open && (
        <div className="okr-picker">
          {['k1', 'k2', 'k3'].map(k => (
            <button
              key={k}
              className={'okr-picker-row' + (tag === k ? ' is-current' : '')}
              onClick={(e) => { e.stopPropagation(); okrApi.setTag(label, k); setOpen(false); }}
            >
              <span className="okr-picker-dot" style={{background: M[k] && M[k].color}}/>
              <span className="okr-picker-short">{M[k] && M[k].short}</span>
              <span className="okr-picker-name">{okrName(k)}</span>
            </button>
          ))}
          {tag && (
            <button
              className="okr-picker-row okr-picker-untag"
              onClick={(e) => { e.stopPropagation(); okrApi.setTag(label, null); setOpen(false); }}
            >
              <span className="okr-picker-dot okr-picker-dot-x">×</span>
              <span className="okr-picker-name">untag</span>
            </button>
          )}
        </div>
      )}
    </span>
  );
}

