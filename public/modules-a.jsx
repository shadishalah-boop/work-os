// --- Draggable task list ---
function useDraggable(initial) {
  const [items, setItems] = useState(initial);
  const dragId = useRef(null);
  const onDragStart = (id) => (e) => { dragId.current = id; e.dataTransfer.effectAllowed = 'move'; };
  const onDragOver  = (id) => (e) => { e.preventDefault(); e.currentTarget.classList.add('drag-over'); };
  const onDragLeave = (e) => { e.currentTarget.classList.remove('drag-over'); };
  const onDrop      = (id) => (e) => {
    e.preventDefault(); e.currentTarget.classList.remove('drag-over');
    const from = items.findIndex(i => i.id === dragId.current);
    const to   = items.findIndex(i => i.id === id);
    if (from < 0 || to < 0 || from === to) return;
    const next = items.slice();
    const [moved] = next.splice(from, 1);
    next.splice(to, 0, moved);
    setItems(next);
  };
  return { items, setItems, onDragStart, onDragOver, onDragLeave, onDrop };
}

// --- Top-3 Today (hero priority block) ---
function Top3({ data, onToggle, density, okrApi }) {
  const drag = useDraggable(data);
  useEffect(() => { drag.setItems(data); }, [data]);
  const now = new Date();
  const stamp = now.toLocaleString('en-US', { weekday: 'short', month: 'short', day: 'numeric' }).toUpperCase();

  // Score the live Top-3 items (label-keyed lookup so re-orders don't break it)
  const scored = useMemo(() => {
    const map = new Map();
    drag.items.forEach(it => map.set(it.id, scoreTask(it, { bucket: 'top3', okrApi })));
    return map;
  }, [drag.items, okrApi]);

  // Find the highest-scoring open task that is NOT in the Top-3 — if its score
  // beats the lowest-scoring Top-3 item, surface a "swap" suggestion.
  const suggestion = useMemo(() => {
    const all = scoreAllOpen(window.SEED || {}, okrApi);
    const top3Ids = new Set(drag.items.map(i => i.id));
    const candidate = all.find(t => !top3Ids.has(t.id));
    if (!candidate) return null;
    const lowestTop3 = Math.min(...drag.items.filter(i => !i.done).map(i => (scored.get(i.id) || {}).score || 0));
    if (candidate._score <= lowestTop3) return null;
    return { ...candidate, lowestTop3 };
  }, [drag.items, scored, okrApi]);

  return (
    <div className="top3 mod-focus-survivor">
      <div className="top3-h">
        <div className="top3-title">
          <span className="badge-star"><Icon name="lightning" size={14} style={{filter:'brightness(0)'}}/></span>
          What actually matters today
        </div>
        <div className="top3-stamp">{stamp} · {drag.items.filter(i=>!i.done).length} of 3 left</div>
      </div>
      <div className="top3-list">
        {drag.items.map((it, idx) => {
          const s = scored.get(it.id) || { score: 0, reasons: [] };
          return (
            <div key={it.id}
                 className={'top3-item' + (it.done ? ' done' : '')}
                 draggable
                 onDragStart={drag.onDragStart(it.id)}
                 onDragOver={drag.onDragOver(it.id)}
                 onDragLeave={drag.onDragLeave}
                 onDrop={drag.onDrop(it.id)}>
              <div className="top3-num">{String(idx + 1).padStart(2,'0')}</div>
              <div>
                <div className="top3-label"><TextWithPeople text={it.label}/></div>
                <div className="top3-meta"><TextWithPeople text={it.meta}/></div>
              </div>
              <div style={{display:'flex', alignItems:'center', gap:8}}>
                <OkrTagger label={it.label} meta={it.meta} okrApi={okrApi}/>
                <div className="top3-check" onClick={() => onToggle(it.id)} role="button" aria-label={it.done ? 'Mark incomplete' : 'Mark complete'}/>
              </div>
            </div>
          );
        })}
      </div>
      {suggestion && (
        <div className="top3-suggest" title={(suggestion._reasons || []).join(' · ')}>
          <span className="ts-tag">Algo suggests</span>
          <span className="ts-body">
            <strong><TextWithPeople text={suggestion.label}/></strong>
            <span className="ts-meta"><TextWithPeople text={suggestion.meta || ''}/></span>
          </span>
        </div>
      )}
    </div>
  );
}

